// 1) create functions for the basic math operations: add, subtract, multiply, divide, exponent
function add(a, b) {
    return a + b;
}
function subtract(a, b) {
    return a - b;
}
function multiply(a, b) {
    return a * b
}
function divide(a, b) {
    return a / b
}
function exponent(a, b) {
    return a ** b
}
